#A variable is a container for data that holds a value.For example:
x=10                #x is the variable and 10 is the value
print(x)

#Assigning multiple variables to multiple values
a,b,c=5,3.2,'Hello'  #a=5,b=3.2 and c='Hello'
print(a,b,c)

#Assigning same values to multiple variables
num_1=num_2=9        # both num_1 and num_2 will have a value of 9
print(num_1)

