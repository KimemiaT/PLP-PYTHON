#COMPARISON OPERATORS-compares two values/variables and returns a boolean, or FALSE
a=12
b=18
equal=a==b         #  False             A-80+  B-60+ C-50+ D-40+
notEqual=a!=b       #  True                 
greaterThan=a>b     #False
lessThan=a<b        #True
gtEqual=a>=b        #  False
ltEqual=a<=b        #True
print(equal,notEqual,greaterThan,lessThan,gtEqual,ltEqual)
