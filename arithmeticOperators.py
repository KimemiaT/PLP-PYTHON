'''Python has the following operators:
1.Arithmetic
2.Assignment
3.Comparison
4.Logical
5.Bitwise
6.Special
'''
#ARITHMETIC e.g +,-,/,*,//,%,**
a=9
b=5
sum=a+b     #addition(+)
diff=a-b    #Subtraction(-)
quotient=a/b    #division(/)
product=a*b     #multiplication(*)
floorDivision=a//b
modulus=a%b     #Modulo
power=a**2      #power
print(sum,diff,quotient,product,floorDivision,modulus,power)

